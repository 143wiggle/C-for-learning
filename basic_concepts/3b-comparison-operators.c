#include <stdio.h>

int main() {
    int x = 5;
    int y = 10;

    printf("x == y: %d\n", x == y);
    printf("x != y: %d\n", x != y);
    printf("x > y: %d\n", x > y);
    printf("x < y: %d\n", x < y);
    printf("x >= y: %d\n", x >= y);
    printf("x <= y: %d\n", x <= y);

    return 0;
}
//purpose:
//Teaches: ==, !=, >, <, >=, <=
//Note: results are 0 for false, 1 for true
